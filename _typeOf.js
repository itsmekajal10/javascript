// Variable, Data Type, typeof Operator

var bankName;
console.log("Value of variable -->bankName:", bankName);
bankName = 'SBI Bank';
console.log("bankName: SBI Bank,", "type:",typeof bankName);


var marks = '90%';
console.log("Value of variable-->marks:", marks);
console.log("Datatype of variable-->marks:", typeof marks);

var isWorking;
console.log("Value of variable -->isWorking:", isWorking);

isWorking = true;
console.log("isWorking: true,", "type:",typeof isWorking);

var totalCount = "Hundred and Seven";
console.log("Value of variable-->totalCount:", totalCount);
console.log("Datatype of variable-->totalCount:", typeof totalCount);

totalCount = 107;
console.log("Value of variable-->totalCount:", totalCount);
console.log("Datatype of variable-->totalCount:", typeof totalCount);


